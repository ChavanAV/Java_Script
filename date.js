

let date = new Date();

//getDay():-
console.log(date.getDay());

switch (date.getDay()) {

    case 1: console.log('mon');
        break;
    case 1: console.log('mon');
        break;
    case 2: console.log('tue');
        break;
    case 3: console.log('web');
        break;
    case 4: console.log('thu');
        break;
    case 5: console.log('fri');
        break;
    case 6: console.log('sat');
        break;
    case 0: console.log('sun');
        break;
}

//getMonth():-
console.log(date.getMonth());

switch (date.getMonth()) {

    case 1: console.log('Feb');
        break;
    case 2: console.log('Mar');
        break;
    case 3: console.log('Apr');
        break;
    case 4: console.log('May');
        break;
    case 5: console.log('Jun');
        break;
    case 6: console.log('Jul');
        break;
    case 7: console.log('Aug');
        break;
    case 8: console.log('Sep');
        break;
    case 9: console.log('Oct');
        break;
    case 10: console.log('Nov');
        break;
    case 11: console.log('Des');
        break;
    case 0: console.log('Jan');
        break;
}

//getDate():- returns todays date
console.log(date.getDate());

//getHours():- time in hours
console.log(date.getHours());

//getMilliseconds():-
console.log(date.getMilliseconds());

//getMinutes():-
console.log(date.getMinutes());

//getSeconds():-
console.log(date.getSeconds());

//getFullYear():-
console.log(date.getFullYear()); //2024

//getTime():- complete date
console.log(date.getTime());    //1728286742245

